#include <iostream>

using namespace std;

int main() {

  int continent;

  cout << "Select your continent" << endl << "1 (EU), 2 (US), 3 (UK)";
  cin >> continent;
  switch (continent) {
    case 1:
        cout << "EU" << endl;
        break;
    case 2:
        cout << "US" << endl;
        break;
    case 3:
        cout << "UK" << endl;
  }
}
